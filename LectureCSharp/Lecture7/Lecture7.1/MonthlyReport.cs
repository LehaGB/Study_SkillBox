        /// Название месяца.
        /// </summary>
        public string Month;


        /// <summary>
        /// Доход
        /// </summary>
        public int Income;


        /// <summary>
        /// Расход
        /// </summary>
        public int Consumption;


        /// <summary>
        /// Прибыль
        /// </summary>
        public int Arrived;
    }
}

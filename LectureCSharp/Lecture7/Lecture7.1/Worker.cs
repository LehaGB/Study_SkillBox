using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Lecture7._1
{
    /// <summary>
    /// Структура, описывающего рабочего.
    /// </summary>
    struct Worker
    {

        /// <summary>
        /// Должность.
        /// </summary>
        public string Position;


        /// <summary>
        /// Зарплата.
        /// </summary>
        public int Salary;


        /// <summary>
        /// Имя
        /// </summary>
        public string FirstName;


        /// <summary>
        /// Фамилия.
        /// </summary>
        public string LastName;


        /// <summary>
        /// Дата рождения.
        /// </summary>
        public DateTime DateOfBirth;
    }
}
